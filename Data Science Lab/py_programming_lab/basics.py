# Variables
num = -1
pi = 3.14
text = "this is a python lab"

print(num, pi, text)

# Operations
add = num + 5
sum = num - 2
mul = num * 3
div = num / 4

print(add, sum, mul, div)

# Conditionals
if num > 5:
    print("Greater than 5")
elif num < 5:
    print ("Less than 5")
else:
    print("Equal to 5")

#Loops
for i in range(1,11):
    print(i)